import lombok.Data;
import lombok.NoArgsConstructor;

import java.io.Serializable;

@Data
@AllArgsConstructor
@NoArgsConstructor
public class User implements Serializable {
    @JSONField(serialize = false)
    private Integer userid;
    private String username;
    private String password;
}
